const supabase = createClient(supabaseUrl, supabaseAnonKey);

async function runSeed() {
  console.log('🔌 Conectando a Supabase...');
  
  // Limpiar centros anteriores
  console.log('🧹 Limpiando registros anteriores de centros de acopio...');
  const { error: deleteError } = await supabase
    .from('centros_acopio')
    .delete()
    .neq('id', 0); // Borra todo
     
  if (deleteError) {
    console.log('⚠️ Nota: No se pudieron borrar los registros de centros de acopio previos.', deleteError.message);
  }

  // Limpiar enlaces anteriores
  console.log('🧹 Limpiando registros anteriores de enlaces de ayuda...');
  const { error: deleteEnlacesError } = await supabase
    .from('enlaces_ayuda')
    .delete()
    .neq('id', 0); // Borra todo
     
  if (deleteEnlacesError) {
    console.log('⚠️ Nota: No se pudieron borrar los registros de enlaces previos.', deleteEnlacesError.message);
  }

  console.log('📥 Subiendo centros de acopio en lotes...');
  
  // Insertar en lotes de 50 para evitar sobrecargar la petición
  const batchSize = 50;
  for (let i = 0; i < centersToInsert.length; i += batchSize) {
    const batch = centersToInsert.slice(i, i + batchSize);
    const { error } = await supabase
      .from('centros_acopio')
      .insert(batch);
      
    if (error) {
      console.error(`❌ Error insertando lote ${i} a ${i + batch.length}:`, error);
      process.exit(1);
    } else {
      console.log(`✅ Lote ${i + 1} a ${Math.min(i + batchSize, centersToInsert.length)} subido con éxito.`);
    }
  }

  console.log('📥 Subiendo enlaces de ayuda...');
  const { error: insertEnlacesError } = await supabase
    .from('enlaces_ayuda')
    .insert(fallbackEnlaces);
    
  if (insertEnlacesError) {
    console.error('❌ Error insertando enlaces de ayuda:', insertEnlacesError);
    process.exit(1);
  } else {
    console.log('✅ Enlaces de ayuda sembrados con éxito.');
  }
  
  console.log('\n🎉 ¡Siembra completada con éxito!');
}


runSeed();
