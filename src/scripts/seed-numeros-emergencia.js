import fs from 'fs';
import path from 'path';
import { createClient } from '@supabase/supabase-js';

function loadEnv() {
  const envPath = path.resolve(process.cwd(), '.env');
  if (!fs.existsSync(envPath)) {
    console.log('⚠️ No se encontró archivo .env local. Usando variables de entorno del sistema.');
    return;
  }
  const content = fs.readFileSync(envPath, 'utf8');
  for (const line of content.split('\n')) {
    const trimmed = line.trim();
    if (!trimmed || trimmed.startsWith('#')) continue;
    const idx = trimmed.indexOf('=');
    if (idx === -1) continue;
    const key = trimmed.substring(0, idx).trim();
    let val = trimmed.substring(idx + 1).trim();
    if ((val.startsWith('"') && val.endsWith('"')) || (val.startsWith("'") && val.endsWith("'"))) {
      val = val.substring(1, val.length - 1);
    }
    process.env[key] = val;
  }
}

loadEnv();

const supabaseUrl = process.env.PUBLIC_SUPABASE_URL;
const supabaseAnonKey = process.env.PUBLIC_SUPABASE_ANON_KEY;
const supabaseServiceKey = process.env.SUPABASE_SERVICE_ROLE_KEY;
const supabaseWriteKey = supabaseServiceKey || supabaseAnonKey;

const CREATE_TABLE_SQL = `
-- =========================================================================
-- COPIAR Y PEGAR EN EL SQL EDITOR DE SUPABASE PARA CREAR LA TABLA Y POLÍTICAS
-- =========================================================================

create table public.numeros_emergencia (
  id bigint generated by default as identity primary key,
  categoria text not null,
  subcategoria text,
  nombre text not null,
  numero text,
  orden integer not null default 0,
  verificado boolean default true,
  created_at timestamp with time zone default timezone('utc'::text, now()) not null,
  constraint chk_ne_categoria_len check (char_length(categoria) between 2 and 60),
  constraint chk_ne_subcategoria_len check (subcategoria is null or char_length(subcategoria) <= 60),
  constraint chk_ne_nombre_len check (char_length(nombre) between 1 and 200),
  constraint chk_ne_numero_len check (numero is null or char_length(numero) <= 200)
);

alter table public.numeros_emergencia enable row level security;

create policy "Permitir lectura pública de numeros_emergencia" on public.numeros_emergencia
  for select using (true);

create index if not exists idx_numeros_emergencia_categoria on public.numeros_emergencia (categoria);
create index if not exists idx_numeros_emergencia_orden on public.numeros_emergencia (categoria, subcategoria, orden);
`;

console.log(CREATE_TABLE_SQL);

const rawDataPath = path.resolve(process.cwd(), 'src/data/numerosemergencia.json');
const rawData = JSON.parse(fs.readFileSync(rawDataPath, 'utf8'));
const te = rawData.telefonos_emergencia;

const rows = [];
let orden = 0;

for (const item of te.numeros_nacionales) {
  rows.push({
    categoria: 'nacional',
    subcategoria: null,
    nombre: item.servicio,
    numero: item.numero,
    orden: orden++,
    verificado: true,
  });
}

const caracas = te.caracas;
const subgrupos = ['bomberos', 'proteccion_civil', 'rescate', 'policia'];
for (const sub of subgrupos) {
  let i = 0;
  for (const item of caracas[sub] || []) {
    rows.push({
      categoria: 'caracas',
      subcategoria: sub,
      nombre: item.nombre,
      numero: item.telefono,
      orden: i++,
      verificado: true,
    });
  }
}

let r = 0;
for (const freq of caracas.radio_informacion || []) {
  rows.push({
    categoria: 'caracas',
    subcategoria: 'radio_informacion',
    nombre: freq,
    numero: null,
    orden: r++,
    verificado: true,
  });
}

console.log(`📊 Total de números de emergencia procesados para insertar: ${rows.length}`);

if (!supabaseUrl || !supabaseWriteKey) {
  console.log('\n❌ ERROR: Falta PUBLIC_SUPABASE_URL o una key de escritura.');
  console.log('La anon key no puede escribir (RLS). Usa la service_role key SOLO por variable de entorno:');
  console.log("SUPABASE_SERVICE_ROLE_KEY='...' node src/scripts/seed-numeros-emergencia.js");
  process.exit(1);
}

const supabase = createClient(supabaseUrl, supabaseWriteKey);

async function runSeed() {
  console.log(supabaseServiceKey
    ? '🔌 Conectando a Supabase (service_role: salta RLS)...'
    : '🔌 Conectando a Supabase (anon key — fallará el insert si RLS bloquea escritura)...');

  const { error: probeError } = await supabase
    .from('numeros_emergencia')
    .select('id')
    .limit(1);

  if (probeError) {
    console.log('\n❌ La tabla "numeros_emergencia" no existe o no es accesible:', probeError.message);
    console.log('👉 Primero crea la tabla pegando el SQL de arriba en el SQL Editor de Supabase, luego vuelve a ejecutar este script.');
    process.exit(1);
  }

  console.log('🧹 Limpiando registros anteriores de números de emergencia...');
  const { error: deleteError } = await supabase
    .from('numeros_emergencia')
    .delete()
    .neq('id', 0);

  if (deleteError) {
    console.log('⚠️ Nota: No se pudieron borrar los registros previos.', deleteError.message);
  }

  console.log('📥 Subiendo números de emergencia en lotes...');
  const batchSize = 50;
  for (let i = 0; i < rows.length; i += batchSize) {
    const batch = rows.slice(i, i + batchSize);
    const { error } = await supabase
      .from('numeros_emergencia')
      .insert(batch);

    if (error) {
      console.error(`❌ Error insertando lote ${i} a ${i + batch.length}:`, error);
      process.exit(1);
    } else {
      console.log(`✅ Lote ${i + 1} a ${Math.min(i + batchSize, rows.length)} subido con éxito.`);
    }
  }

  console.log('\n🎉 ¡Siembra de números de emergencia completada con éxito!');
}

runSeed();
